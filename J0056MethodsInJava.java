public class J0056MethodsInJava {
    
    static void func(int a, int b) {
        if(a>b)
        {
            System.out.println("A is greater than B");
        }
        else
        {
            System.out.println("A is less than B");
        }
    }
  
    public static void main(String[] args) {
        func(7,4);
    } 
  }
